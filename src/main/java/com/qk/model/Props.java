package com.qk.model;

public class Props {
    private String propsId;

    private String propsName;

    private String propsRole;

    private String propsLevel;

    private String propsDescribe;

    public String getPropsId() {
        return propsId;
    }

    public void setPropsId(String propsId) {
        this.propsId = propsId;
    }

    public String getPropsName() {
        return propsName;
    }

    public void setPropsName(String propsName) {
        this.propsName = propsName;
    }

    public String getPropsRole() {
        return propsRole;
    }

    public void setPropsRole(String propsRole) {
        this.propsRole = propsRole;
    }

    public String getPropsLevel() {
        return propsLevel;
    }

    public void setPropsLevel(String propsLevel) {
        this.propsLevel = propsLevel;
    }

    public String getPropsDescribe() {
        return propsDescribe;
    }

    public void setPropsDescribe(String propsDescribe) {
        this.propsDescribe = propsDescribe;
    }
}